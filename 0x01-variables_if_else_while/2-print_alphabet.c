#include <stdio.h>
/**
 * main - Entry point of the program
 *
 * Description: This program generates a random number and
 * checks if the last number is zero, greater than 5 or not
 *
 * Return: 0 (Success)
 */
int main(void)
{
	int i;

	for (i = 97; i <= 122; i++)
		putchar(i);
	putchar('\n');
return (0);
}
